#include<stdio.h>
/* This program illustrates the increment operator */
int main()
{
  int x, y;
  
  x = 6;
  y = 6;
   
   printf("Register Number: RA2211042010030");

  printf("The value of ++x is %d\n", ++x );
  printf("The value of y++ is %d\n", y++ );
  
  printf("After the increment x is %d\n", x);
  printf("After the increment y is %d\n", y );
  }
